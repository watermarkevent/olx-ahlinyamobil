export function getBrands(): string[] {
  return [...new Set(carDatabase.map((c) => c.brand))].sort()
}

export function getModelsByBrand(brand: string): string[] {
  return [...new Set(carDatabase.filter((c) => c.brand === brand).map((c) => c.model))].sort()
}

export function getYearsByBrandModel(brand: string, model: string): number[] {
  return carDatabase
    .filter((c) => c.brand === brand && c.model === model)
    .map((c) => c.year)
    .sort((a, b) => b - a)
}

export function getOtrPrice(brand: string, model: string, year: number): number | null {
  const entry = carDatabase.find(
    (c) => c.brand === brand && c.model === model && c.year === year
  )
  if (entry) return entry.otrPrice

  // Nearest year fallback
  const candidates = carDatabase
    .filter((c) => c.brand === brand && c.model === model)
    .sort((a, b) => Math.abs(a.year - year) - Math.abs(b.year - year))

  return candidates[0]?.otrPrice ?? null
}
